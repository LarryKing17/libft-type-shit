#include "libft.h"

size_t	ft_strlcat(char *dst, const char *src, size_t dsts)
{
	size_t	dlen;
	size_t	slen;
	size_t	i;

	dlen = 0;
	slen = 0;
	i = 0;
	while (dst[dlen] != '\0' && dlen < dsts)
		dlen++;
	while (src[slen])
		slen++;
	if (dsts <= dlen)
		return (dsts + slen);
	i = 0;
	while (src[i] != '\0' && dlen + i < dsts - 1)
	{
		dst[dlen + i] = src[i];
		i++;
	}
	dst[dlen + i] = '\0';
	return (dlen + slen);
}
